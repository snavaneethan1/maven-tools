public class Definition {

    //Meta-Inf location inside jar
    public static final String META_INF_LOCATION = File.separator+"META-INF"+File.separator;


    /** Holds the finalResource of definition **/
    private String finalResource;

    /** Holds the configuration name of the definition **/
    private String configFileName;

    /** Start Tag to include in the definition **/
    private String startTag;

    /** End Tag to include in the definition **/
    private String endTag;

    /** Holds the name of the definition **/
    private String definitionType;

    public Definition(String definitionType, String finalResource, String configFileName, String startTag, String endTag){
        this.definitionType = definitionType;
        this.finalResource = finalResource;
        this.configFileName = configFileName;
        this.startTag = startTag;
        this.endTag = endTag;
    }


    public String getFinalResource() {
        return finalResource;
    }

    public void setFinalResource(String finalResource) {
        this.finalResource = finalResource;
    }

    public String getConfigFileName() {
        return configFileName;
    }

    public void setConfigFileName(String configFileName) {
        this.configFileName = configFileName;
    }

    public String getStartTag() {
        return startTag;
    }

    public void setStartTag(String startTag) {
        this.startTag = startTag;
    }

    public String getEndTag() {
        return endTag;
    }

    public void setEndTag(String endTag) {
        this.endTag = endTag;
    }

    public String getDefinitionType() {
        return definitionType;
    }

    public void setDefinitionType(String definitionType) {
        this.definitionType = definitionType;
    }

    public File getResourceFromMetaInf(File classesDirectory){
        return new File(classesDirectory + META_INF_LOCATION + getFinalResource());
    }

}
